// GITHUB INVERTED
const githubIconInvert = document.getElementById('githubIconInverted');

githubIconInvert.addEventListener('mouseover', () => {
    githubIconInvert.src = "./images/icons/github-hover.svg"

})

githubIconInvert.addEventListener('mouseout', () => {
    githubIconInvert.src = "./images/icons/github-inverted.svg"

})


